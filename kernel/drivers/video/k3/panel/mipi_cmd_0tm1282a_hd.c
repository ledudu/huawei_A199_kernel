static int mipi_otm1282a_panel_check_esd(struct platform_device *pdev)
{
	struct k3_fb_data_type *k3fd = NULL;
	int ret_AC = 0, read_num_AC = 0;

	BUG_ON(pdev == NULL);
	k3fd = (struct k3_fb_data_type *)platform_get_drvdata(pdev);
	BUG_ON(k3fd == NULL);

	if (cnt < 5) 
	{
		cnt++;
		//Read REG 0xAC
		outp32(k3fd->edc_base + MIPIDSI_GEN_HDR_OFFSET, 0xAC << 8 | 0x06);
		ret_AC = inp32(k3fd->edc_base + MIPIDSI_GEN_PLD_DATA_OFFSET);
		return 0;
	} 
	else 
	{
		do{
			//Read REG 0xAC
			outp32(k3fd->edc_base + MIPIDSI_GEN_HDR_OFFSET, 0xAC << 8 | 0x06);
			ret_AC = inp32(k3fd->edc_base + MIPIDSI_GEN_PLD_DATA_OFFSET);
			read_num_AC++;
		}while((ret_AC != 0)&&(read_num_AC < 3));
	
		if ((ret_AC !=0)&&(read_num_AC >= 3))
		{
			return 1;
		}
		else
		{
			return 0;
		}
	}
}




